CREATE VIEW Popular_Book(Popular_ISBN,Popular_Title)
AS SELECT ISBN,Title
FROM BOOK
WHERE Title IN
(SELECT Title
FROM (SELECT Title, SUM(CQuantity)
FROM BOOK,CONTAIN
WHERE ISBN=CISBN 
GROUP BY ISBN
HAVING SUM(CQuantity)>(SELECT AVG(sum) FROM
(SELECT SUM(CQuantity) AS sum
FROM BOOK,CONTAIN
WHERE ISBN=CISBN
GROUP BY ISBN))));
