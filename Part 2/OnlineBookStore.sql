SELECT ISBN,Title,Discount_percent
FROM SALE, SALE_DETAIL,BOOK
WHERE ISBN=SISBN AND SPromo_Code=Promo_Code AND ISBN IN
(SELECT SISBN
FROM(SELECT SISBN,SUM(Amount)
FROM SALE_DETAIL,SALE
WHERE SPromo_Code=Promo_Code
GROUP BY SISBN, Discount_percent
HAVING SUM(Amount)> (SELECT AVG(sum) FROM
(SELECT SUM(Amount) AS sum
FROM SALE_DETAIL, SALE
WHERE SPromo_Code=Promo_Code
GROUP BY SISBN,Discount_percent))));
